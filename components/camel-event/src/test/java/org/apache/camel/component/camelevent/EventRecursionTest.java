package org.apache.camel.component.camelevent;

import org.apache.camel.builder.RouteBuilder;
import org.apache.camel.component.mock.MockEndpoint;
import org.apache.camel.test.junit6.CamelTestSupport;
import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.assertTrue;

public class EventRecursionTest extends CamelTestSupport {

    @Test
    void testNoInfiniteRecursion() throws Exception {
        // Subscribe to ALL exchange events including the ones generated by the event consumer itself.
        // Without recursion protection, this would cause a StackOverflowError.
        MockEndpoint mock = getMockEndpoint("mock:recursion");
        mock.expectedMinimumMessageCount(1);

        // Send a message to trigger exchange events
        template.sendBody("direct:recursionSource", "Hello");

        mock.assertIsSatisfied();

        // The fact that we got here without a StackOverflowError proves
        // the recursion protection works. The number of events should be
        // bounded (not infinite).
        assertTrue(mock.getExchanges().size() < 100,
                "Should not have an excessive number of events (got " + mock.getExchanges().size() + ")");
    }

    @Test
    void testRecursionWithAllWildcard() throws Exception {
        // The wildcard * route also subscribes to all events
        MockEndpoint mock = getMockEndpoint("mock:recursionAll");
        mock.expectedMinimumMessageCount(1);

        template.sendBody("direct:recursionSource", "Hello");

        mock.assertIsSatisfied();

        assertTrue(mock.getExchanges().size() < 100,
                "Should not have an excessive number of events (got " + mock.getExchanges().size() + ")");
    }

    @Override
    protected RouteBuilder createRouteBuilder() {
        return new RouteBuilder() {
            @Override
            public void configure() {
                // This route subscribes to exchange events - processing these creates
                // new exchange events, which could cause infinite recursion
                from("event:ExchangeCreated,ExchangeCompleted")
                        .routeId("recursionConsumer")
                        .to("mock:recursion");

                from("event:*")
                        .routeId("recursionAllConsumer")
                        .to("mock:recursionAll");

                from("direct:recursionSource").routeId("recursionSourceRoute")
                        .log("Processing recursion source");
            }
        };
    }
}
