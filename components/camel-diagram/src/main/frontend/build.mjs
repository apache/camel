import * as esbuild from 'esbuild';
import { mkdirSync, readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const outfile = resolve(__dirname, '../resources/META-INF/resources/camel/diagram/camel-route-diagram.js');
mkdirSync(dirname(outfile), { recursive: true });

await esbuild.build({
  entryPoints: [resolve(__dirname, 'src/camel-route-diagram.js')],
  bundle: true,
  minify: true,
  format: 'esm',
  legalComments: 'external',
  outfile,
});

console.log('Built:', outfile);

// Regenerate THIRD-PARTY-NOTICES.txt from the .LEGAL.txt sidecar produced by esbuild
// so that it stays in sync with the actual bundled packages.
