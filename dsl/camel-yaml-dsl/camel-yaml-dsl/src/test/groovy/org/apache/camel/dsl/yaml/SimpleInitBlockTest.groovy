package org.apache.camel.dsl.yaml

import org.apache.camel.component.mock.MockEndpoint
import org.apache.camel.dsl.yaml.support.YamlTestSupport

class SimpleInitBlockTest extends YamlTestSupport {

    def "initBlock"() {
        setup:
        loadRoutes '''
                - route:
                    from:
                      uri: direct:map
                      steps:    
                        - setBody:
                            simple:
                              expression: |-
                                $init{
                                  // this is a java like comment
                                  $$sum := ${sum(${header.lines},100)}
                    
                                  $$sku := ${iif(${body} contains 'Camel',123,999)}
                                  ## and here is also a yaml like comment
                                }init$
                                orderId=$$sku,total=$$sum
                        - to:
                            uri: mock:result
                        '''
        withMock('mock:result') {
            expectedMessageCount(1)
            message(0).body().contains("orderId=123,total=208")
        }

        when:
        context.start()

        withTemplate {
            to('direct:map').withHeader("lines", "75,33").withBody('Hello Camel').send()
        }

        then:
        MockEndpoint.assertIsSatisfied(context)
    }

}
