#!/usr/bin/env bash
# Snapshot every doc symlink (and nav.adoc file) under the dirs the doc-prep
# pipeline writes into. Emits a deterministic, sortable text manifest:
#
#   SYMLINK  <repo-relative-path>  <readlink-target>
#   NAV      <repo-relative-path>  <sha256-of-file>
#
# Used to byte-compare gulp's output (on main) against the new mojo's output
# (on the CAMEL-23492 branch). Run from a repo root.

set -euo pipefail

if [[ $# -ne 1 ]]; then
    echo "usage: $0 <repo-root>" >&2
    exit 2
fi

ROOT="$1"
cd "$ROOT"

# The 6 destination roots the mojo writes into. Keep in sync with sources() in
# PrepareDocSymlinksMojo. nav.adoc files live next to (parent of) each pages dir.
ROOTS=(
    "docs/components/modules/ROOT"
    "docs/components/modules/dataformats"
    "docs/components/modules/languages"
    "docs/components/modules/others"
    "docs/user-manual/modules/faq/examples"
    "core/camel-core-engine/src/main/docs/modules/eips"
)

# Use sha256sum on Linux, shasum on macOS.
if command -v sha256sum >/dev/null 2>&1; then
    HASH="sha256sum"
else
    HASH="shasum -a 256"
fi

for r in "${ROOTS[@]}"; do
    if [[ ! -d "$r" ]]; then continue; fi
    # Symlinks: path + target text.
    find "$r" -type l -print0 \
        | LC_ALL=C sort -z \
        | while IFS= read -r -d '' f; do
            printf 'SYMLINK\t%s\t%s\n' "$f" "$(readlink "$f")"
          done
    # nav.adoc files (regular files, generated by createNav): path + content hash.
    find "$r" -type f -name 'nav.adoc' -print0 \
        | LC_ALL=C sort -z \
        | while IFS= read -r -d '' f; do
            printf 'NAV\t%s\t%s\n' "$f" "$($HASH "$f" | awk '{print $1}')"
          done
done
